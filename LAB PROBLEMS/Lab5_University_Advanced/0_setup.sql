DROP TABLE IF EXISTS prereq; DROP TABLE IF EXISTS advisor; DROP TABLE IF EXISTS takes;
DROP TABLE IF EXISTS teaches; DROP TABLE IF EXISTS section; DROP TABLE IF EXISTS course;
DROP TABLE IF EXISTS instructor; DROP TABLE IF EXISTS student; DROP TABLE IF EXISTS department;
CREATE TABLE department (dept_name VARCHAR(20), building VARCHAR(15), budget NUMERIC(12,2), PRIMARY KEY (dept_name));
CREATE TABLE instructor (ID VARCHAR(5), name VARCHAR(20), dept_name VARCHAR(20), salary NUMERIC(8,2), PRIMARY KEY (ID), FOREIGN KEY (dept_name) REFERENCES department(dept_name));
CREATE TABLE student (ID VARCHAR(5), name VARCHAR(20), dept_name VARCHAR(20), tot_cred NUMERIC(3,0), PRIMARY KEY (ID), FOREIGN KEY (dept_name) REFERENCES department(dept_name));
CREATE TABLE course (course_id VARCHAR(8), title VARCHAR(50), dept_name VARCHAR(20), credits NUMERIC(2,0), PRIMARY KEY (course_id), FOREIGN KEY (dept_name) REFERENCES department(dept_name));
CREATE TABLE section (course_id VARCHAR(8), sec_id VARCHAR(8), semester VARCHAR(6), year NUMERIC(4,0), building VARCHAR(15), room_number VARCHAR(7), time_slot_id VARCHAR(4), PRIMARY KEY (course_id, sec_id, semester, year), FOREIGN KEY (course_id) REFERENCES course(course_id));
CREATE TABLE teaches (ID VARCHAR(5), course_id VARCHAR(8), sec_id VARCHAR(8), semester VARCHAR(6), year NUMERIC(4,0), PRIMARY KEY (ID, course_id, sec_id, semester, year), FOREIGN KEY (course_id, sec_id, semester, year) REFERENCES section(course_id, sec_id, semester, year), FOREIGN KEY (ID) REFERENCES instructor(ID));
CREATE TABLE takes (ID VARCHAR(5), course_id VARCHAR(8), sec_id VARCHAR(8), semester VARCHAR(6), year NUMERIC(4,0), grade VARCHAR(2), PRIMARY KEY (ID, course_id, sec_id, semester, year), FOREIGN KEY (ID) REFERENCES student(ID), FOREIGN KEY (course_id, sec_id, semester, year) REFERENCES section(course_id, sec_id, semester, year));
